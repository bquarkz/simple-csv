package com.bquarkz.simplecsv;

public interface MappingCSV
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Static fields
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Static Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    static MappingCSV mapping( String columnName )
    {
        return mapping( columnName, Object::toString );
    }

    static MappingCSV mapping( String columnName, MapperCSV mapper )
    {
        return new MappingCSV()
        {
            @Override
            public String columnName()
            {
                return columnName;
            }

            @Override
            public MapperCSV mapper()
            {
                return mapper;
            }
        };
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Default Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Contracts
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    String columnName();
    MapperCSV mapper();
}

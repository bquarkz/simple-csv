package com.bquarkz.simplecsv;

public interface MappingBean
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Static fields
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Static Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    static MappingBean mapping( final String columnName, final MapperBean mapper )
    {
        return new MappingBean()
        {
            @Override
            public String columnName()
            {
                return columnName;
            }

            @Override
            public MapperBean mapper()
            {
                return mapper;
            }
        };
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Default Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Contracts
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    String columnName();
    MapperBean mapper();
}

package com.bquarkz.simplecsv;

import static com.bquarkz.simplecsv.CSVUtils.desembrace;

public class CSVBaseAutoMapper implements CSVAutoMapper
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Constants
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Special Fields And Injections
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Fields
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Constructors
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public CSVBaseAutoMapper()
    {
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Factories
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Getters And Setters
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public Object map(
            Class< ? > type,
            String delimitersContent,
            String content )
    {
        final String extract = desembrace( delimitersContent, content );
        if( type == Integer.class )
        {
            return MapperBean.mapInteger( extract );
        }
        else if( type == Double.class )
        {
            return MapperBean.mapDouble( extract );
        }
        else if( type == Long.class )
        {
            return MapperBean.mapLong( extract );
        }
        else if( type == Float.class )
        {
            return MapperBean.mapFloat( extract );
        }
        else if( type == String.class )
        {
            return extract;
        }
        else
        {
            return extract; // by default return content as a String
        }
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Inner Classes And Patterns
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
